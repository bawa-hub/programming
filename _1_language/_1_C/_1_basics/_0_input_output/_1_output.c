#include <stdio.h>
int main()
{
    int testInteger = 5;
    float number1 = 13.5;
    double number2 = 12.4;
    char chr = 'a';

    printf("Number = %d", testInteger);
    printf("\n");
    printf("number1 = %f\n", number1);
    printf("number2 = %lf\n", number2);
    printf("character = %c", chr);

    return 0;
}